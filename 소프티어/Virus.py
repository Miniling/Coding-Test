import sys

input = sys.stdin.readline

# 바이러스가 초당 P배씩 증가
# K마리의 바이러스가 N초 후에는 몇 마리로 불어나는지 (죽는 바이러스는 없다고 가정)
# 마리 수 K, 증가율 P, 총 시간 N
K, P, N = map(int, input().split())

# K*P**N%1000000007 으로 계산하면, 값이 큰 예제에는 시간초과 발생
# K*P%1000000007 을 N초 동안 진행하면, 매번 나머지 연산을 진행하는 것이라 값이 커져도 문제없음!
for _ in range(N):
    K = K*P%1000000007

# 총 바이러스 개수를 1000000007개로 나눈 나머지 출력
print(K)